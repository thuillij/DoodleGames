    public void afficher(){
        for(int i = 0 ; i < 3 ; i++)
        {
               for(int j = 0 ; j < 3 ; j++)
               {
                   if(this.mod.getCase(i,j) == 1)
                       System.out.print(" X ");
                   else if(this.mod.getCase(i,j) == 2)
                       System.out.print(" 0 ");
                   else
                       System.out.print(" _ ");

                   if( j != 2 )
                       System.out.print("|");
               }
            if( i != 2 )
                System.out.print("\n---+---+---\n");
        }
        System.out.println("\n\n");
    }

